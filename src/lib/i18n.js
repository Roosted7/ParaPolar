const LANG_PATH_RE = /^\/(fr|de|nl)(?=\/|$)/;

/** Strip a language prefix from a pathname ("/fr/x" -> "/x"). */
export function stripLangPath(pathname) {
  return pathname.replace(LANG_PATH_RE, "");
}

/** Build the pathname for a language ("/x", "fr" -> "/fr/x"). */
export function langPath(pathname, lang) {
  const bare = stripLangPath(pathname) || "/";
  return lang === "en" ? bare : `/${lang}${bare === "/" ? "" : bare}`;
}

export function detectLang() {
  // 1) URL path prefix (/fr, /de, /nl) — an explicit link wins
  try {
    const m = window.location.pathname.match(LANG_PATH_RE);
    if (m) return m[1];
  } catch {
    /* ignore */
  }

  // 2) persisted preference
  try {
    const ls = localStorage.getItem("pp_lang");
    if (LANGS.includes(ls)) return ls;
  } catch {
    /* ignore */
  }

  // 3) cookie from the edge redirect (pp_lang=en|de|fr|nl)
  try {
    const m = document.cookie.match(/(?:^|; )pp_lang=(en|de|fr|nl)/);
    if (m) return m[1];
  } catch {
    /* ignore */
  }

  // 4) navigator fallback
  const nav = (typeof navigator !== "undefined" && navigator.language) || "en";
  for (const lang of ["fr", "de", "nl"]) {
    if (nav.startsWith(lang)) return lang;
  }
  return "en";
}
